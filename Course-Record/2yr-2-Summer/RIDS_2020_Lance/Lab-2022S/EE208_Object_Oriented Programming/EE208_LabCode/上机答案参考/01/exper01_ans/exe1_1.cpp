/*
Design, write, compile, and run a program that determines the work, W, performed by a piston engine providing a force of 1000 N over a distance of 15 centimeters. 
The following formula is used to determine the work performed:
W=F*d
*/

#include <bits/stdc++.h>
using namespace std;

int main()
{
    int F = 1000;    //1000N
    double d = 0.15; //15cm=0.15m
    cout << F * d << endl;
    return 0;
}